			<p>
				There have been numerous studies done on gender equality across industries and countries; however, there aren't many that focus on education, especially among universities. One study that stands out in this category is "The Glass Door Remains Closed: Another Look at Gender Inequality in Undergraduate Business Schools" by Laura Marini Davis and Victoria Geyfman<sup>1</sup>. The authors looked at female representation in undergraduate business schools by analyzing accredited U.S. business programs in the Association to Advance Collegiate Schools of Business (AACSB) from 2003 to 2011.
				<br><br>
				According to AACSB data, the number of male students enrolled increased approximately 15% from 2010 to 2013 while the number of female students fell slightly by 0.55%. The data also shows that female representation at accredited member institutions in AACSB in the United States declined 3.6% from 2003 to 2011. The authors also took a look at the degree attainment and noticed that even though the number of bachelor business degrees increased by 9% during 2003 through 2011, the number of degrees awarded to female students remained virtually the same, increasing only 0.21%. The authors concluded, female representation in undergraduate business schools measured by either enrollment or degrees awarded has declined in the last decade –remarkable findings in light of a nationally reported reversal in the gender gap.</p>
